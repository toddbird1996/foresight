export function CookieConsent() {
  const [visible, setVisible] = React.useState(false);

  React.useEffect(() => {
    const consent = localStorage.getItem('cookie-consent');
    if (!consent) {
      setVisible(true);
    }
  }, []);

  const accept = () => {
    localStorage.setItem('cookie-consent', 'accepted');
    setVisible(false);
  };

  const decline = () => {
    localStorage.setItem('cookie-consent', 'declined');
    setVisible(false);
    // Disable non-essential cookies/tracking
  };

  if (!visible) return null;

  return (
    <div className="fixed bottom-0 left-0 right-0 p-4 bg-slate-900 border-t border-slate-800 z-50">
      <div className="max-w-7xl mx-auto flex flex-col sm:flex-row items-center justify-between gap-4">
        <p className="text-sm text-slate-400">
          We use cookies to improve your experience. By using Foresight, you agree to our{' '}
          <a href="/privacy" className="text-orange-400 underline">Privacy Policy</a>.
        </p>
        <div className="flex gap-3">
          <button
            onClick={decline}
            className="px-4 py-2 border border-slate-700 rounded-lg text-sm hover:bg-slate-800"
          >
            Decline
          </button>
          <button
            onClick={accept}
            className="px-4 py-2 bg-orange-500 rounded-lg text-sm font-medium hover:bg-orange-600"
          >
            Accept
          </button>
        </div>
      </div>
    </div>
  );
}

